#include<stdio.h>

int main() {
    int a = 1;
    float b = 5;
    char c = 'k';

    // Print each variable on a new line
    printf("%d\n", a);  // Prints integer a with newline
    printf("%f\n", b);  // Prints float b with newline
    printf("%c\n", c);  // Prints character c with newline

    return 0;
}
